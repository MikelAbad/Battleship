package packCasillas;

public class Coordenada {
	private int x;
	private int y;

	public Coordenada(int pX, int pY) {
		this.x = pX;
		this.y = pY;
	}
	
	public int getX() {return x;}
	public void setX(int pX) {this.x = pX;}
	
	public int getY() {return y;}
	public void setY(int pY) {this.y = pY;}	
}
